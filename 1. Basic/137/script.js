"use strict";
// Write a JavaScript program to test whether a given integer
// is greater than 15 return the given number, otherwise return 15.
function testFifteen(num) {
    return num > 15 ? num : 15;
}
console.log(testFifteen(123));
console.log(testFifteen(10));
console.log(testFifteen(5));
// 123
// 15
// 15
